#Arithmetic Operators - Simple Calculations

num1 = 45
num2 = 20


print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #modulus- Returns remainder


#comparison operators

x = 78
y = 60

print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y)
print(x != y)


#Assignment Operators -used to assign values to a variable

number = 40
print(number)

a = 35
a += 2
print(a)

result = 90 - (6 * 3) / 2 + 5  #operator Precedence
print(result)

#Logical Operators -and , or , not
print(90 < 40 and 67 > 34)
print(90 < 40 or 67 > 34)
print(not(90 < 40 or 67 > 34))


